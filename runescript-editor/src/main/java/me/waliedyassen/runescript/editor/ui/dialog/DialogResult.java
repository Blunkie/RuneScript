package me.waliedyassen.runescript.editor.ui.dialog;

/**
 * An enum holding all of the possible option dialog results.
 *
 * @author Walied K. Yassen
 */
public enum DialogResult {

    /**
     * The "Yes" option was selected in the dialog.
     */
    YES,

    /**
     * The "No" option was selected in the dialog.
     */
    NO,

    /**
     * The "Cancel" option was selected in the dialog.
     */
    CANCEL,

}
